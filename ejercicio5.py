# Programa para calcular el área de un rectangulo
print("=== Cálculo del área de un rectangulo ===")

# Solicitar el radio del círculo
base = float(input("Ingresa la base del rectangulo: "))
altura = float(input("Ingresa la altura del rectangulo: "))

# Calcular el área
area = base * altura

# Mostrar resultados
print("\n=== Resultados ===")
print(f"Área del resctangulo: {area:.2f}")
